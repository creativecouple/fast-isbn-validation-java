package de.creativecouple.validation.isbn;

public final class Hyphens {
    private static final boolean[] hyphens = new boolean[Character.MAX_VALUE + 1];

    static {
        for (char c : new char[]{0x002D, 0x005F, 0x00AD, 0x00AF, 0x02C9, 0x02CD, 0x02D7, 0x0320, 0x2010, 0x2011, 0x2012, 0x2013, 0x2014, 0x2015, 0x203E, 0x2043, 0x2052, 0x207B, 0x208B, 0x2212, 0x2216, 0x23AF, 0x23E4, 0x2500, 0x2501, 0x2796, 0x29FF, 0x2E3A, 0x2E3B, 0xFE63, 0xFF0D}) {
            hyphens[c] = true;
        }
    }

    public static boolean isHyphen(char ch) {
        return hyphens[ch];
    }

}
